/*Operator Overloading general syntax---
---> return-type class-name::operator operator-symbol(argument-list)
{
operations to be performed

}*/

//In the above syntax operator is the keyword.
//In general return-type will be the class for which the operator function is a member but, it can return any type.

/* Protocol to be followed

1) The precedence of the operator cannot be altered or changed
2) The number of operands that an operator takes cannot be altered or changed.

*/

